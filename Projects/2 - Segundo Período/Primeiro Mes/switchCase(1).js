// Classic switch case

let dayOfWeek = 3;

switch (dayOfWeek) {
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Wednesday");
        break;
    // Other cases...
    default:
        console.log("Invalid day");
        break;
};